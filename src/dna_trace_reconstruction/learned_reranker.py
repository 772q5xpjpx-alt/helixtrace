def _validate_dna(name: str, sequence: str) -> None:
    if not isinstance(sequence, str):
        raise TypeError(f"{name} must be a string")
    invalid_bases = sorted(set(sequence) - set(DNA_ALPHABET))
    if invalid_bases:
        raise ValueError(f"{name} contains invalid DNA bases: {invalid_bases}")


def _validate_inputs(
    traces: Sequence[str], candidates: Mapping[str, str]
) -> tuple[tuple[str, ...], dict[str, str]]:
    if isinstance(traces, str | bytes) or not isinstance(traces, Sequence):
        raise TypeError("traces must be a sequence of DNA strings")
    if not traces:
        raise ValueError("traces must contain at least one DNA trace")
    normalized_traces = tuple(traces)
    for index, trace in enumerate(normalized_traces):
        _validate_dna(f"traces[{index}]", trace)

    if not isinstance(candidates, Mapping):
        raise TypeError("candidates must be a mapping")
    expected_names = set(METHOD_NAMES)
    observed_names = set(candidates)
    if observed_names != expected_names:
        missing = sorted(expected_names - observed_names)
        extra = sorted(observed_names - expected_names)
        raise ValueError(
            f"candidates require exactly {METHOD_NAMES}; missing={missing}, extra={extra}"
        )

    normalized_candidates = {name: candidates[name] for name in METHOD_NAMES}
    for name, sequence in normalized_candidates.items():
        _validate_dna(f"candidates[{name!r}]", sequence)
    return normalized_traces, normalized_candidates


def _mean_pairwise_trace_distance(traces: tuple[str, ...]) -> float:
    distances = [
        normalized_edit_distance(first, second)
        for first_index, first in enumerate(traces)
        for second in traces[first_index + 1 :]
    ]
    return statistics.fmean(distances) if distances else 0.0


def extract_candidate_features(
    traces: Sequence[str], candidates: Mapping[str, str]
) -> dict[str, tuple[float, ...]]:
    """Return source-free feature vectors for the four named candidates."""
    normalized_traces, normalized_candidates = _validate_inputs(traces, candidates)
    trace_length_median = statistics.median(len(trace) for trace in normalized_traces)
    scale = max(1.0, trace_length_median)
    pairwise_distance = _mean_pairwise_trace_distance(normalized_traces)
    trace_length_spread = (
        max(map(len, normalized_traces)) - min(map(len, normalized_traces))
    ) / scale
    inverse_cluster_size = 1.0 / len(normalized_traces)

    evidence_costs = {
        name: evidence_cost(sequence, normalized_traces)
        for name, sequence in normalized_candidates.items()
    }
    minimum_evidence_cost = min(evidence_costs.values())
    consensus = normalized_candidates["consensus"]
    medoid = normalized_candidates["medoid"]

    feature_vectors: dict[str, tuple[float, ...]] = {}
    for name in METHOD_NAMES:
        sequence = normalized_candidates[name]
        summary = summarize_biological_constraints(sequence)
        method_indicators = tuple(float(name == method) for method in METHOD_NAMES)
        agreement_fraction = sum(
            candidate == sequence for candidate in normalized_candidates.values()
        ) / len(METHOD_NAMES)
        sequence_length = max(1, len(sequence))

        feature_vectors[name] = (
            evidence_costs[name] / scale,
            (evidence_costs[name] - minimum_evidence_cost) / scale,
            (len(sequence) - trace_length_median) / scale,
            abs(len(sequence) - trace_length_median) / scale,
            levenshtein_distance(sequence, consensus) / max(1, len(consensus)),
            levenshtein_distance(sequence, medoid) / max(1, len(medoid)),
            summary.gc_excess / sequence_length,
            summary.homopolymer_excess / sequence_length,
            float(summary.valid),
            agreement_fraction,
            *method_indicators,
            pairwise_distance,
            trace_length_spread,
            *(indicator * pairwise_distance for indicator in method_indicators),
            *(indicator * inverse_cluster_size for indicator in method_indicators),
        )

    return feature_vectors


def rerank_candidates(
    traces: Sequence[str],
    candidates: Mapping[str, str],
    *,
    model: LinearRerankerModel = DEFAULT_MODEL,
) -> RerankerResult:
    """Select one deterministic reconstruction using the learned linear score.

    Candidate generation remains the responsibility of the existing pipeline.
    The four required names deliberately match its medoid, alignment consensus,
    evidence-only refinement, and biology-aware refinement outputs.
    """
    normalized_traces, normalized_candidates = _validate_inputs(traces, candidates)
    if model.feature_names != FEATURE_NAMES:
        raise ValueError("model feature names do not match this feature extractor")

    feature_vectors = extract_candidate_features(normalized_traces, normalized_candidates)
    scores = tuple(
        CandidateScore(
            name=name,
            sequence=normalized_candidates[name],
            predicted_error_score=model.predict(feature_vectors[name]),
            evidence_cost=evidence_cost(normalized_candidates[name], normalized_traces),
            features=feature_vectors[name],
        )
        for name in METHOD_NAMES
    )
    selected = min(
        scores, key=lambda score: (score.predicted_error_score, METHOD_NAMES.index(score.name))
    )
    return RerankerResult(
        selected_name=selected.name,
        selected_sequence=selected.sequence,
        model_version=model.version,
        scores=scores,
    )
