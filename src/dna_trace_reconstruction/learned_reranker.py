"""A small supervised reranker for HelixTrace reconstruction candidates.

The model is deliberately simple and inspectable: ridge regression predicts a
normalized-edit-distance-like score from features that are available at
inference time.  It then selects one of the four existing deterministic
reconstruction candidates.  The source strand is used only by the offline
training script to create regression targets; it is never an inference input.

The committed weights were fitted on controlled synthetic data.  This module
does not claim that those data represent a wet-lab sequencing distribution.
"""

