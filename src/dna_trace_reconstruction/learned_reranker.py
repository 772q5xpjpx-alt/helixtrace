@dataclass(frozen=True, slots=True)
class LinearRerankerModel:
    """Versioned parameters for a standardized linear ridge model."""

    version: str
    feature_names: tuple[str, ...]
    feature_means: tuple[float, ...]
    feature_scales: tuple[float, ...]
    intercept: float
    weights: tuple[float, ...]
    training_seed: int
    training_experiments: int
    ridge_alpha: float

    def __post_init__(self) -> None:
        feature_count = len(self.feature_names)
        if not (
            len(self.feature_means) == feature_count
            and len(self.feature_scales) == feature_count
            and len(self.weights) == feature_count
        ):
            raise ValueError("model feature arrays must have equal lengths")
        if any(scale <= 0.0 or not math.isfinite(scale) for scale in self.feature_scales):
            raise ValueError("model feature scales must be finite and positive")

    def predict(self, features: Sequence[float]) -> float:
        """Return the model's unbounded predicted error score (lower is better)."""
        if isinstance(features, str | bytes) or len(features) != len(self.feature_names):
            raise ValueError(f"features must contain {len(self.feature_names)} numeric values")

        score = self.intercept
        for value, mean, scale, weight in zip(
            features,
            self.feature_means,
            self.feature_scales,
            self.weights,
            strict=True,
        ):
            if isinstance(value, bool) or not isinstance(value, Real):
                raise TypeError("model features must be numeric")
            if not math.isfinite(value):
                raise ValueError("model features must be finite")
            score += weight * ((value - mean) / scale)
        return score


@dataclass(frozen=True, slots=True)
class CandidateScore:
    """One candidate and the observable evidence used to score it."""

    name: str
    sequence: str
    predicted_error_score: float
    evidence_cost: float
    features: tuple[float, ...]


@dataclass(frozen=True, slots=True)
class RerankerResult:
    """Deterministic output of learned candidate selection."""

    selected_name: str
    selected_sequence: str
    model_version: str
    scores: tuple[CandidateScore, ...]


